package com.example.wj.pojo;

import org.springframework.stereotype.Repository;

@Repository
public class AdminUserRole {
    int id;
    int rid;
    int uid;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getRid() {
        return rid;
    }

    public void setRid(int rid) {
        this.rid = rid;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }
}
